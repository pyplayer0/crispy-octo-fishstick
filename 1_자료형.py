# 숫자 자료형
print(1)
print(1.340)

# 문자 자료형
print("hello world")
print("안녕하세요")
print("10")

# boolean
print(True)
print(False)

# 자료형 변환
int()   # 정수형
float() # 실수형
str()   # 문자형
bool()  # 불리안형

# ex) 문자형 안에 실수를 정수형으로 변환 할 때
a = '3.5'
print(a) 
print(float(a))
print(int(float(a))) # 실수 > 정수 소수점 버림
